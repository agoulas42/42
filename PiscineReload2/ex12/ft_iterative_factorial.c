int		ft_iterative_factorial(int nb)
{
	int n;
	int i;

	n = 1;
	i = 1;
	if (nb < 0 || nb > 12)
		return (0);
	if (nb == 1 || nb == 0)
		return (1);
	else
	{
		while (i <= nb)
		{
			n = n * i;
			i++;
		}
	}
	return (n);
}
