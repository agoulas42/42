int		ft_sqrt(int nb)
{
	int n;

	n = 2;
	if (nb < 0)
		return (0);
	if (nb == -0 || nb == 0 || nb == 1)
		return (nb);
	while ((n * n) < nb)
	{
		n++;
	}
	if (nb % n == 0)
		return (n);
	else
		return (0);
}
