int		ft_putchar(char c);

int		main(int ac, char **av)
{
	int i;
	int l;

	i = 1;
	if (ac > 1)
	{
		while (av[i] != '\0' && i <= ac)
		{
			l = 0;
			while (av[i][l] != '\0')
			{
				ft_putchar(av[i][l]);
				l++;
			}
			ft_putchar('\n');
			i++;
		}
	}
	return (0);
}
