#include "../includes/ft_fonction.h"

void	ft_putstr(char *str)
{
	int		i;
	char	a;

	a = 'a';
	i = 0;
	while (str[i])
	{
		a = str[i];
		write(1, &a, 1);
		i++;
	}
}
