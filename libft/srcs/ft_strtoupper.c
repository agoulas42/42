#include "../includes/libft.h"

int		ft_strtoupper(char **s)
{
	int i;

	i = 0;
	while (*s[i])
	{
		if (ft_islower(*s[i]) == 1)
			*s[i] = ft_toupper(*s[i]);
		i++;
	}
	return (i);
}
